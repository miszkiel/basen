package konstruktory;

public class Pies {
    private String rasa;
    private String kolorSiersci;
    private String ulubioneZarcie;
    private int iloscLap;
    private boolean czySzczeka;

    public Pies(String rasa, String kolorSiersci, String ulubioneZarcie, int iloscLap, boolean czySzczeka){
        this.rasa = rasa;
        this.kolorSiersci = kolorSiersci;
        this.ulubioneZarcie = ulubioneZarcie;
        this.iloscLap = iloscLap;
        this.czySzczeka = czySzczeka;
    }


}
